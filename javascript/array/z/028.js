// 存储1-100之间的奇数到数组
var array = [];
var j = 0;
for (var i = 0; i < 100; i++) {
    if (i % 2 !== 0) {
        // 数组的下标是不连续的
        array[i] = i;
        // 数组的下标是连续的
        // 运用数组的方法，将奇数追加在空数组中
        array.push(i);
        // 定义一个变量，定义的变量是索引值
        array[j] = i;
        j++;
        // 将数组的索引值写成数组的长度，并将索引值赋值
        array[array.length] = i;
    }
}

